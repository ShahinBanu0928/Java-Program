public class SumandAverage {
    public static void main(String[] args) {
        int A=10;
        int B=20;
        int C=30;
        int D=40;
        int E=50;
        int sum = A + B + C + D + E;
        int avg = sum/5;
        System.out.println("SUM = " + sum);
        System.out.println("AVG = "+ avg);
    }
    
}
