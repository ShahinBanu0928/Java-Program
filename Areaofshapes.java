public class Areaofshapes {
    // Area of Rectangle
    // Area of Rec = length * breadth
    public static void main(String[] args) {
        {
        double length = 30.5;
        double breadth = 20.5;
        double areaOfRec = length * breadth;
        System.out.println("Area of Rectangle = " + areaOfRec);
    }
// Area of Circle
// Area of Circle = 3.14 * radius * radius
{
    double radius = 2.5;
    System.out.println("Area of Circle = " + 3.14 * radius * radius);
}

    
// Area of Triangle 
// Area of Trangle = 1/2 * b * h
{
    double breadth = 5;
    double height = 8;
    System.out.println("Area of Triangle = " + 0.5 * breadth * height);
}

//Area of Parallelogram
// Area of Parallelogram = base * height
{
    double base = 12.0;
    double height= 8.0;
    System.out.println("Area of Parallelogram = " + base * height);
}
}
    
}
